def related_themes_prompt(topic: str, hypotheses: list[str], questions: list[str]) -> str:
    return f"""Тема исследования: {topic}

Гипотезы:
{chr(10).join(f"- {h}" for h in hypotheses) or "(нет)"}

Вопросы:
{chr(10).join(f"- {q}" for q in questions) or "(нет)"}

Предложи 8-15 связанных подтем для фильтрации транскрипта (только по теме исследования).
Верни JSON: {{"themes": ["...", ...]}}"""


def filter_utterances_prompt(
    topic: str,
    related_themes: list[str],
    utterances_json: str,
) -> str:
    themes = ", ".join(related_themes) if related_themes else "(не указаны)"
    return f"""Ты помогаешь размечать транскрипт качественного UX/CX-интервью.

Тема исследования: {topic}
Связанные темы: {themes}

ТИПЫ СЕГМЕНТОВ:
- on_topic: любая содержательная речь о работе, опыте, поведении, задачах, проблемах, инструментах, мнениях, чувствах — даже если тема явно не называется
- intro: приветствие, знакомство, объяснение формата, согласие на запись
- outro: прощание, благодарности, закрытие встречи
- small_talk: бытовая болтовня, не несущая информации об опыте
- off_topic: технические неполадки связи, пустые реакции («ага», «да-да», «ммм»), сугубо организационное

ГЛАВНОЕ ПРАВИЛО для реплик респондента (is_respondent=true):
Если реплика содержит хоть какую-то информацию о том, что человек делает, думает, чувствует, использует или с чем сталкивается в своей работе — это on_topic, даже если слова темы не звучат.
При сомнении — on_topic.

Реплики не-респондентов (is_respondent=false):
Размечай строже. Вопросы модератора — off_topic (не кодируются). Содержательные уточнения — small_talk.

include_in_coding=true только для on_topic.

Реплики:
{utterances_json}

Верни JSON: {{"labels": [{{"index": 0, "segment_type": "on_topic", "include_in_coding": true, "exclude_reason": null}}, ...]}}
Верни метку для КАЖДОЙ реплики в списке."""


def atomize_prompt(respondent_id: str, blocks_json: str) -> str:
    return f"""Атомизируй речь респондента {respondent_id} по правилам UX-кодирования.

ПРАВИЛА АТОМИЗАЦИИ:

1. Один атом = одна мысль, действие, факт или эмоция. Длинную реплику ВСЕГДА дели на отдельные атомы.
   Дели, если в реплике есть: несколько действий; действие + причина; проблема + workaround; оценка + действие; проблема + эмоция.
   Пример: «Я сравниваю цены в нескольких источниках, а если срочно — беру первый вариант»
   → «Сравнивает цены в нескольких источниках перед выбором»
   → «При срочной потребности берёт первый доступный вариант»

2. atom — конкретный наблюдаемый факт, максимум 15–20 слов. Только то, что прямо подтверждается цитатой.
   НЕ начинай с «Респондент...» или «Подтверждает...».
   ❌ «Респондент использует ChatGPT для написания постов»
   ✅ «Использует ChatGPT для создания контента»
   ❌ «Подтверждает, что основной поток клиентов — сарафан»
   ✅ «Основной поток клиентов приходит по рекомендациям»
   ❌ «Пользователи обычно ищут через фильтры» (обобщение — запрещено)
   ✅ «Ищет товар через фильтры, но они сбрасываются при возврате»
   ЗАПРЕЩЕНО: обобщения ("пользователи обычно", "бизнесы делают", "мастера предпочитают"),
   аналитические выводы, объяснения скрытых причин поведения, рекомендации.

3. НЕ создавай атом, если реплика:
   (а) только «да»/«нет»/«ага»/«поняла» — даже если по смыслу что-то подтверждает;
   (б) переспрос без собственного содержания;
   (в) подтверждение чужих слов без добавления нового смысла.
   Для таких реплик верни пустой список [].

4. context — нарративная цепочка со стрелками →, объясняющая КАК разговор пришёл к этой реплике.
   Пиши интерпретацию, не дословные цитаты:
   ✅ «Интервьюер спрашивает о помещении → Респондент описывает офис без вывески → становится ясно, что трафика с улицы нет»
   ❌ «Speaker 1: Где расположены? | Speaker 2: Офисное здание...»

Блоки (context_hint — сырые реплики для ориентира при построении context):
{blocks_json}

Верни JSON: {{"observations": [{{"utterance_index": 0, "atom": "...", "context": "..."}}]}}"""


def code_observations_prompt(observations_json: str) -> str:
    return f"""{CODEBOOK_SUMMARY}

Закодируй каждое наблюдение:

secondary_code — второй код из кодбука, ТОЛЬКО если атом одновременно значим по двум разным слоям.
  Типичные парные комбинации (primary → secondary):
  • Барьер → Незнание возможности: человек не может сделать что-то И причина — незнание существующей функции
  • Барьер → Недоверие/сомнение: блокирующий фактор связан со скептицизмом или неуверенностью
  • Паттерн поведения → Ценность: устойчивое действие, которое пользователь сам называет полезным
  • Драйвер → Ценность: что-то одновременно ускоряет задачу И осознаётся как явная польза
  • Проблема → Незнание возможности: человек обходит проблему, не зная о готовом решении
  Если второго слоя нет — null.

modality_tags — 1-2 тега, ТОЛЬКО если в тексте атома есть явные речевые маркеры:
  «приходится», «вынужден» → Необходимость; «невозможно», «не могу» → Невозможность;
  «раздражает», «бесит», «ужасно» → Оценка−; «нравится», «удобно», «круто» → Оценка+;
  «уверен», «точно» → Уверенность; «вроде», «не уверен» → Сомнение; и т.д.
  Если маркеров нет или атом слишком короткий — верни [].

kind — "Инсайт" если атом раскрывает глубинную причину поведения или мышления (почему человек так делает/думает), иначе "Наблюдение".

primary_code — один основной код из кодбука.
  ИСКЛЮЧЕНИЕ: если атом описывает профиль или бэкграунд респондента (кто он, каков его бизнес, стаж, масштаб, сегмент, личные характеристики) — это не поведенческое наблюдение, ставь primary_code="-".

is_key_task — true если атом касается ключевой задачи, ради которой пользователь использует продукт.

jtbd — одна строка: задача/работа ("job"), которую пользователь пытается выполнить в этом атоме.
  ✅ «Найти материал по нужной цене быстро», «Записать клиента без созвона», «Отчитаться перед партнёром»
  Если JTBD не читается из атома — null.

workaround — есть ли в атоме обходной способ решения проблемы?
  "Да" — пользователь описывает workaround явно.
  "Нет" — проблема есть, но workaround не упомянут.
  "—" — атом не о проблеме/барьере.

{observations_json}

Верни JSON: {{"coded": [{{"temp_id": 0, "primary_code": "...", "secondary_code": null, "modality_tags": [], "kind": "Наблюдение", "is_key_task": false, "jtbd": null, "workaround": "—"}}]}}"""


AFFINITY_NAMING_RULES = """
ПРАВИЛА ИМЕНОВАНИЯ AFFINITY-КЛАСТЕРОВ

Хорошее название — это инсайт, а не категория.

❌ Слабо (просто ярлык, не двигает анализ):
  «Навигация», «Проблемы с онбордингом», «Обратная связь»

✅ Сильно (передаёт наблюдение, напряжение или паттерн):
  «Пользователи теряются после первого входа, потому что не понимают, что делать дальше»
  «Доверие к продукту рушится, когда система не подтверждает действие»
  «Обходят основной флоу, чтобы сэкономить время»

Тест: если в названии можно поставить «потому что» или «когда» — оно работает.

Форматы, которые работают:
- Наблюдение + контекст: «Сравнивают цены вне продукта, потому что внутри нет фильтров»
- Напряжение / противоречие: «Хотят кастомизацию, но боятся сломать настройки»
- Поведенческий паттерн: «Первый шаг всегда — поиск, не меню»
- JTBD-формулировка: «Нужно быстро убедиться, что всё прошло»

Практические правила:
1. Пиши от лица пользователя или о пользователе — не о системе
2. Один кластер = одна идея; если есть «и» — скорее всего, это два кластера
3. Избегай глагола «использовать» — он ничего не говорит о смысле
4. Название должно быть утверждением, с которым можно поспорить
"""


def normalize_clusters_prompt(clusters_json: str) -> str:
    return f"""Тебе дан список названий affinity-кластеров. Они были созданы независимо для разных \
частей данных, поэтому одно и то же явление может быть названо по-разному в разных кластерах.

Задача: для каждого кластера найди его каноническое название.

Правила:
1. Объединяй кластеры, которые описывают ОДИН И ТОТ ЖЕ паттерн или явление, даже если \
формулировки отличаются.
2. Из группы похожих выбирай или создавай одно каноническое название — самое точное и инсайтовое.
3. Если кластер уникален и не имеет двойника — canonical совпадает с original.
4. Итоговых уникальных canonical должно быть существенно меньше, чем входных кластеров.

Кластеры (JSON-массив строк):
{clusters_json}

Верни JSON: {{"mapping": [{{"original": "...", "canonical": "..."}}]}}
Обязательно укажи запись для КАЖДОГО входного кластера без исключения."""


def affinity_prompt(atoms_json: str) -> str:
    return f"""{AFFINITY_NAMING_RULES}

Сгруппируй атомы в affinity-кластеры. Для каждого атома укажи:
- affinity_cluster: название-инсайт по правилам выше (одна чёткая идея, можно поспорить)
- normalized_category: широкая тематическая категория (2-4 слова).

КЛЮЧЕВЫЕ КАТЕГОРИИ — используй их в первую очередь:
  «Профиль» — наблюдения о бэкграунде и профиле респондента/бизнеса: кто он, стаж, масштаб, сегмент, структура бизнеса. Эти атомы описывают контекст, а не поведение.
  «Маркетинг» — каналы привлечения клиентов, продвижение, реклама, лояльность.
  «Инструменты» — сервисы, приложения, платформы, которые использует бизнес.
  «Бизнес-процессы» — операционная деятельность: закупки, учёт, расписание, коммуникация с клиентами.
  «Финансы» — платежи, тарифы, затраты, налоги.
Создавай другие категории, если тема выходит за рамки этих пяти.

ГРУППИРОВКА: объединяй атомы на одну тему в один кластер. Не создавай кластер из одного атома, если другой кластер уже покрывает эту тему — добавь атом туда.

{atoms_json}

Верни JSON: {{"clusters": [{{"temp_id": 0, "affinity_cluster": "...", "normalized_category": "..."}}]}}"""
