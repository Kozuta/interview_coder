def related_themes_prompt(topic: str, hypotheses: list[str], questions: list[str]) -> str:
    return f"""Тема исследования: {topic}

Гипотезы:
{chr(10).join(f"- {h}" for h in hypotheses) or "(нет)"}

Вопросы:
{chr(10).join(f"- {q}" for q in questions) or "(нет)"}

Предложи 8-15 связанных подтем для фильтрации транскрипта (только по теме исследования).
Верни JSON: {{"themes": ["...", ...]}}"""


def filter_utterances_prompt(
    topic: str,
    related_themes: list[str],
    utterances_json: str,
) -> str:
    themes = ", ".join(related_themes) if related_themes else "(не указаны)"
    return f"""Ты помогаешь размечать транскрипт качественного UX/CX-интервью.

Тема исследования: {topic}
Связанные темы: {themes}

ТИПЫ СЕГМЕНТОВ:
- on_topic: любая содержательная речь о работе, опыте, поведении, задачах, проблемах, инструментах, мнениях, чувствах — даже если тема явно не называется
- intro: приветствие, знакомство, объяснение формата, согласие на запись
- outro: прощание, благодарности, закрытие встречи
- small_talk: бытовая болтовня, не несущая информации об опыте
- off_topic: технические неполадки связи, пустые реакции («ага», «да-да», «ммм»), сугубо организационное

ГЛАВНОЕ ПРАВИЛО для реплик респондента (is_respondent=true):
Если реплика содержит хоть какую-то информацию о том, что человек делает, думает, чувствует, использует или с чем сталкивается в своей работе — это on_topic, даже если слова темы не звучат.
При сомнении — on_topic.

Реплики не-респондентов (is_respondent=false):
Размечай строже. Вопросы модератора — off_topic (не кодируются). Содержательные уточнения — small_talk.

include_in_coding=true только для on_topic.

Реплики:
{utterances_json}

Верни JSON: {{"labels": [{{"index": 0, "segment_type": "on_topic", "include_in_coding": true, "exclude_reason": null}}, ...]}}
Верни метку для КАЖДОЙ реплики в списке."""


def atomize_prompt(respondent_id: str, blocks_json: str) -> str:
    return f"""Атомизируй речь респондента {respondent_id} по правилам UX-кодирования.

ПРАВИЛА АТОМИЗАЦИИ:
1. Один атом = одна мысль, действие, факт или эмоция. Никогда не объединяй несколько мыслей.
2. Длинную реплику ВСЕГДА дели на столько атомов, сколько в ней отдельных мыслей (3-5+ атомов из одной реплики — нормально).
   Пример:
   Реплика: «Я сравниваю цены в нескольких источниках, а если срочно — беру первый вариант»
   → Атом 1: atom="Сравнивает цены в нескольких источниках"
   → Атом 2: atom="При срочности берёт первый доступный вариант"
3. atom — суть одним коротким предложением от третьего лица, без вводных слов.
4. Если высказывание содержит условие, меняющее смысл («если», «когда», «потому что») — включи его в атом или выдели отдельным.
5. НЕ создавай атом, если реплика: (а) только «да»/«нет»/«ага»/«поняла» и подобное; (б) переспрос без собственного содержания; (в) эхо-повтор предыдущей реплики. Для таких реплик верни пустой список.

Для каждого атома укажи:
- utterance_index: индекс исходной реплики
- atom: суть одним предложением от третьего лица
- context: 2-3 фразы диалога ДО этой реплики (формат «Speaker N: текст»)
- content: описание по схеме «что делает/говорит/чувствует → почему (если ясно из текста) → что из этого следует → что пытается сделать или как решить (если применимо)».
  ВАЖНО: включай только то, что явно следует из текста. Если часть схемы не ясна — просто пропусти её, не выдумывай.

Блоки:
{blocks_json}

Верни JSON: {{"observations": [{{"utterance_index": 0, "atom": "...", "context": "...", "content": "..."}}]}}"""


def code_observations_prompt(observations_json: str) -> str:
    return f"""{CODEBOOK_SUMMARY}

Закодируй каждое наблюдение:

primary_code — один основной код из кодбука (обязательно).

secondary_code — второй код из кодбука, ТОЛЬКО если атом одновременно значим по двум разным слоям.
  Типичные парные комбинации (primary → secondary):
  • Барьер → Незнание возможности: человек не может сделать что-то И причина — незнание существующей функции
  • Барьер → Недоверие/сомнение: блокирующий фактор связан со скептицизмом или неуверенностью
  • Паттерн поведения → Ценность: устойчивое действие, которое пользователь сам называет полезным
  • Драйвер → Ценность: что-то одновременно ускоряет задачу И осознаётся как явная польза
  • Проблема → Незнание возможности: человек обходит проблему, не зная о готовом решении
  Если второго слоя нет — null.

modality_tags — 1-2 тега, ТОЛЬКО если в тексте атома есть явные речевые маркеры:
  «приходится», «вынужден» → Необходимость; «невозможно», «не могу» → Невозможность;
  «раздражает», «бесит», «ужасно» → Оценка−; «нравится», «удобно», «круто» → Оценка+;
  «уверен», «точно» → Уверенность; «вроде», «не уверен» → Сомнение; и т.д.
  Если маркеров нет или атом слишком короткий — верни [].

kind — "insight" если атом раскрывает глубинную причину поведения или мышления (почему человек так делает/думает), иначе "observation".

is_key_task — true если атом касается ключевой задачи, ради которой пользователь использует продукт.

{observations_json}

Верни JSON: {{"coded": [{{"temp_id": 0, "primary_code": "...", "secondary_code": null, "modality_tags": [], "kind": "observation", "is_key_task": false}}]}}"""


AFFINITY_NAMING_RULES = """
ПРАВИЛА ИМЕНОВАНИЯ AFFINITY-КЛАСТЕРОВ

Хорошее название — это инсайт, а не категория.

❌ Слабо (просто ярлык, не двигает анализ):
  «Навигация», «Проблемы с онбордингом», «Обратная связь»

✅ Сильно (передаёт наблюдение, напряжение или паттерн):
  «Пользователи теряются после первого входа, потому что не понимают, что делать дальше»
  «Доверие к продукту рушится, когда система не подтверждает действие»
  «Обходят основной флоу, чтобы сэкономить время»

Тест: если в названии можно поставить «потому что» или «когда» — оно работает.

Форматы, которые работают:
- Наблюдение + контекст: «Сравнивают цены вне продукта, потому что внутри нет фильтров»
- Напряжение / противоречие: «Хотят кастомизацию, но боятся сломать настройки»
- Поведенческий паттерн: «Первый шаг всегда — поиск, не меню»
- JTBD-формулировка: «Нужно быстро убедиться, что всё прошло»

Практические правила:
1. Пиши от лица пользователя или о пользователе — не о системе
2. Один кластер = одна идея; если есть «и» — скорее всего, это два кластера
3. Избегай глагола «использовать» — он ничего не говорит о смысле
4. Название должно быть утверждением, с которым можно поспорить
"""


def affinity_prompt(atoms_json: str) -> str:
    return f"""{AFFINITY_NAMING_RULES}

Сгруппируй атомы в affinity-кластеры. Для каждого атома укажи:
- affinity_cluster: название-инсайт по правилам выше (одна чёткая идея, можно поспорить)
- normalized_category: широкая категория 2-4 слова, объединяющая несколько кластеров одной темы

Разные атомы с одной темой — в один кластер. Разные по смыслу — разные кластеры.

{atoms_json}

Верни JSON: {{"clusters": [{{"temp_id": 0, "affinity_cluster": "...", "normalized_category": "..."}}]}}"""
